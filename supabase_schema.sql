-- RPC: Increment Balance
CREATE OR REPLACE FUNCTION increment_balance(user_id UUID, amount_to_add REAL)
RETURNS void AS $$
BEGIN
  UPDATE users
  SET balance = balance + amount_to_add
  WHERE id = user_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- RPC: Decrement Balance
CREATE OR REPLACE FUNCTION decrement_balance(user_id UUID, amount_to_sub REAL)
RETURNS void AS $$
BEGIN
  UPDATE users
  SET balance = balance - amount_to_sub
  WHERE id = user_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;
